let circleRadius = 10;

let circleArea = Math.PI * Math.pow(circleRadius, 2);
let circleCircumference = Math.PI * 2 * circleRadius;

console.log("For a circle with a radius of " + circleRadius + " units:");

console.log(" - The area is approximately " + circleArea + " square units.");
console.log(
  " - The circumference is approximately " + circleCircumference + " units."
);
